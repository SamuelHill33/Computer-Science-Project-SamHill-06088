function preload() {
    userImage = loadImage('http://127.0.0.1:5500/assets/sprite.png'); //preloads character image
}

function setup() { //function executes once on startup
    createCanvas(mazeMap.length * size, mazeMap.length * size); //sets canvas size to array size
    grid = new Grid(mazeMap); //creates new grid object
    playerGridRef = new GridRef(0 * size, 0 * size, size); //creates player grid reference
    player = new UserSprite(userImage, playerGridRef, speed, size); //creates player sprite
}

function draw() { //function executes every tick (ms)
    grid.draw(); //calls draw method in class Grid
    newPosition = player.newPosition(); //calls the move method in class UserSprite
    console.log(newPosition);
    tile = grid.getTile(newPosition);
    if (!tile.isCollidable()) {
        player.setGridRef(newPosition);
    }
    player.draw();
}

